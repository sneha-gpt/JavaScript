const fs = require('fs');
const path = require('path');

const filePath = path.join(__dirname, "hello.txt");

// Write File 

fs.writeFile(filePath, "Hello gyz!!", function(err){
    if(err)
        throw err;
    else
        console.log("Done");
})

// Read File 
fs.readFile(filePath, function(err, data){
    if(err) throw err;
    else console.log(data.toString("utf8"));
})


// Copy File 
const path1 = path.join(__dirname, "hii.txt");

fs.copyFile(filePath, path1, function(err){
    if(err) throw err;
    else console.log("Copied");
})

const path2 = path.join(__dirname, "hey.txt");


// // Rename File 
fs.rename(path1, path2, function(err){
    if(err) throw err;
    else console.log("Name changed");
})


// // Delete File 

fs.unlink(path2, (err) => {
    if(err) throw err;
    else console.log("Deleted");
})

// Delete Directory 

const folderPath = path.join(__dirname, "Folder");

fs.rmdir(folderPath, (err) => {
    if(err) throw err;
    else console.log("Deleted");
})

// If not empty

fs.rmdir(folderPath, {recursive: true}, (err) => {
    if(err) throw err;
    else console.log("Deleted");
})
